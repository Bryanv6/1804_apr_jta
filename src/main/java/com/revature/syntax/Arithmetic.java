package com.revature.syntax;

// Question 4
public class Arithmetic {

	public int addTwo(int a, int b) {
		return a + b;
	}
	
	public int subtractTwo(int a, int b) {
		return a - b;
	}
	
	public int multiplyTwo(int a, int b) {
		return a * b;
	}
	
	public int divideTwo(int a, int b) {
		return a / b;
	}
}
